# A for loop is used for iterating over a sequence

x = 5

# the range function starts at 0 and adds 1 each time by default. So in this case, the range is from 0 to 49. 
# Using for loop, to print the value of x multiplied by the value of i, for up to 50 iterations.
for i in range(50):
    print(x * i)
